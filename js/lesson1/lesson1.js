//функция перевода температуры с градусов Цельсия
// в градусы по Фаренгейту
function toFarengeyt() {
    let celsiy = +prompt("Введите температуру в градусах Цельсия:");
    let farengeyt = (9 / 5) * celsiy + 32;
    alert(`Температура в градусах по Фаренгейту составляет ${farengeyt}`);
}

